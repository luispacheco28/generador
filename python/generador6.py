import random, string, json

i=0
data = [ {
    "book":"ra",
    "code":"ARCAAPTUC8I210121"
} ]
data2 = [ {"codigo": ""

} ]
while i!=99999:
    x = ''.join(random.choices(string.ascii_letters + string.digits, k=10))
    x = "ARCR"+x
    data.append({
    "book":"601c1e14045fe769429057db",
    "code":x
})
    data2.append({"codigo":x})
    i=i+1


with open('mateactividades1.json', 'w') as file:
    json.dump(data, file, indent=4)
with open('cod_mateactividades1.json', 'w') as file:
    json.dump(data2, file, indent=4)


